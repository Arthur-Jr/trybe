// src/Cars.jsx

import React, { useContext } from 'react';
import MoveContext from './context/moveContext';
import carBlue from './images/carBlue.jpeg';
import carRed from './images/carRed.jpeg';
import carYellow from './images/carYellow.jpeg';

function Cars() {
  const { red, moveRed, blue, moveBlue, yellow, moveYellow } = useContext(MoveContext);

  return (
    <div>
      <div>
        <img
          className={red ? 'car-right' : 'car-left'}
          src={carRed}
          alt="red car"
        />
        <button
          onClick={() => moveRed(!red)}
          type="button"
        >
          Move
        </button>
      </div>
      <div>
        <img
          className={blue ? 'car-right' : 'car-left'}
          src={carBlue}
          alt="blue car"
        />
        <button
          onClick={() => moveBlue(!blue)}
          type="button"
        >
          Move
        </button>
      </div>
      <div>
        <img
          className={yellow ? 'car-right' : 'car-left'}
          src={carYellow}
          alt="yellow car"
        />
        <button
          onClick={() => moveYellow(!yellow)}
          type="button"
        >
          Move
        </button>
      </div>
    </div>
  );
}

export default Cars;
